// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function min(num1, num2) {
  if(num1 < num2){
    return num1;
  } else if (num2 < num1){
    return num2;
  } else if (num1 === num2){
    return num1;
  }

}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(num) {
  if (num % 2 === 0){
    return true;
  } else {
    return false;
  }

}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(str, letter) {
  let splitStr = str.split('');
  let count = 0
  for(let i = 0; i < splitStr.length; i++){
    if (splitStr[i] === letter){
      count++;
    }
  }
  return count;
}



////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(str) {
  let splitStr = str.split('');
  let count = 0;
  for(let i = 0; i < splitStr.length; i++){
    if(splitStr[i] == 'B'){
      count++
    }
  }
  return count;
}

